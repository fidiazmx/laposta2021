                    <div class="dez-post-media dez-img-effect zoom-slow"> <a href="#"><img src="images/banner/bnr3.jpg" alt=""></a> </div>
                    <div class="dez-post-text">
                        <p>
                        Nuestro país tiene una privilegiada posición internacional con una producción anual de alrededor de 38 millones de toneladas anuales.
                        </p>
                        <p>
                        Del total de la producción de alimentos balanceados en el país, el 50% es destinado al sector avícola; 15.9%, al porcícola; 15.7%, al ganado lechero; 11.5%, al ganado de engorda, y 3% a mascotas. El resto se distribuye en acuacultura y otros.
                        </p>
                        <p>
                        En atención a una invitación, al recorrer las instalaciones de la planta de alimentos y acopio de granos “BEGAN” ubicada en Carretera Baca - Sacapuc, el Delegado de la SAGARPA en Yucatán, Luis Ernesto Martínez Ordaz, precisó que el país es el cuarto productor mundial en la materia, luego de Estados Unidos, China y Brasil.
                        </p>
                        <p>
                        BEGAN es una empresa dedicada a la producción de alimento intensivo para ganado, borrego y suplementos para animal de corral, con productos para engorda, sementales, borrego, suplemento, desarrollo, lecheros y potrero.
                        </p>
                        <p>
                        Durante el recorrido se señaló que  a través del proceso de transformación que lleva a cabo la industria de los alimentos balanceados, se traslada la proteína vegetal hacia el sector pecuario, lo que ayuda a generar productos de origen animal, sanos y de calidad.
                        </p>
                        <p>
                        Al respecto, Martínez Ordaz indicó que contar con alimentos balanceados de alta calidad permite elevar la competitividad y productividad del sector.
                        </p>
                        <p>
                        El funcionario federal refrendó el compromiso de la SAGARPA en coadyuvar a través de sus programas y componentes  con los productores en temas de investigación, producción y comercialización a fin de contribuir a la buena nutrición y el bienestar de los animales, que permita que el Estado siga siendo un referente en la producción de la alimentos sanos y de calidad.
                        </p>

                        <p>Fuente: https://bmeditores.mx/ganaderia/articulos-ganaderia/mexico-ya-es-5-productor-mundial-de-alimentos-balanceados-para-animales/</p>
