                    </div>
                    <div class="dez-post-media dez-img-effect zoom-slow"> <a href="#"><img src="images/banner/bnr2.jpg" alt=""></a> </div>
                    <div class="dez-post-text">
                        <p>
                        La producción anual de alimentos balanceados para animales ha mantenido un crecimiento promedio de 4% durante los últimos cuatro años, lo cual ha generado beneficios directos, principalmente al sector pecuario, al cubrir la demanda de esta materia prima que requieren para garantizar una proteína animal de calidad para los mexicanos.
                        </p>
                        <p>
                        Sin embargo, este sector no solo es esencial para mantener la estabilidad en el suministro de alimentos para la industria de animales productivos, sino también para los de compañía, que han cobrado un papel relevante dentro del núcleo familiar, sobre todo durante la pandemia al contribuir al bienestar de la población en general.
                        </p>
                        <p>
                        Durante varios años, nuestro país se mantuvo como el sexto productor global de alimentos balanceados para animales. En su última edición, la Encuesta Global sobre Alimento Balanceado de Alltech 2021, destacó que México subió una posición en el ranking mundial y hoy está en la quinta posición, con una producción cercana a 38 millones de toneladas métricas.
                        </p>
                        <p>
                        Otro dato importante que encontramos en la encuesta es que la producción de alimento balanceado creció 1% a nivel mundial respecto a 2019, al lograr 1,187.7 millones de toneladas métricas producidas. De la región de América Latina, Brasil, México y Argentina forman parte del top 10 global.
                        </p>
                        <p>
                        Genaro Bernal, Director General del Consejo Nacional de Alimentos Balanceados y Nutrición Animal A.C. (CONAFAB) comentó: “La información de la reciente Encuesta Global sobre Alimento Balanceado de Alltech, demuestra el dinamismo y el papel de nuestro sector para contribuir a la seguridad alimentaria de la población. Los números de nuestro país, subraya la gran contribución de nuestra industria al desarrollo económico del país”.
                        </p>
                        <p>
                        El directivo también destaca el compromiso que tiene la industria con la investigación, producción y formulación de alimentos balanceados que garanticen la salud, la inocuidad, la productividad y el desarrollo social y productivo de México, acción que se realiza en las cerca de 600 plantas productoras de alimentos balanceados que hay en el país.
                        </p>
                        <p>
                        En la Encuesta Alltech encontramos, específicamente en el sector pecuario, que México, Chile, Argentina y Colombia registraron un aumento en la producción de alimento balanceado para porcinos, por la demanda de este tipo de proteína durante la contingencia sanitaria por Covid-19. Lo anterior se debe a lo accesible que es este tipo de carne por sus precios moderados.
                        </p>
                        <p>
                        Para CONAFAB la seguridad alimentaria no solo tiene que ver con la cantidad de alimento, también involucra calidad, inocuidad y trazabilidad del producto en todas las fases de la producción de proteína animal así como en la alimentación de pequeñas especies.
                        </p>
                        <p>Fuente: https://bmeditores.mx/ganaderia/articulos-ganaderia/mexico-ya-es-5-productor-mundial-de-alimentos-balanceados-para-animales/</p>
